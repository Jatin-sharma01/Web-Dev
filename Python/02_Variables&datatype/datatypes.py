# Primarily these are the following data types in Python:
# 1. Integers
# 2. Floating point numbers
# 3. Strings
# 4. Booleans
# 5. None

a = 1 # a is an integer
b = 5.22 # b is a floating point number
c = "David" # c is a string
d = True # d is boolean variable, value can be True and false only
e = None # e is a none type variable

